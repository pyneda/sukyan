var wafServerPatterns = map[WAFProvider][]string{
	WAFCloudflare:  {"cloudflare"},
	WAFAkamai:      {"akamaighost", "akamai"},
	WAFCloudFront:  {"cloudfront"},
	WAFImperva:     {"imperva", "incapsula"},
	WAFSucuri:      {"sucuri/cloudproxy", "sucuri"},
	WAFDataDome:    {"datadome"},
	WAFModSecurity: {"mod_security"},
}

// IdentifyWAF detects which WAF/CDN protects a target based on response headers.
func IdentifyWAF(headers map[string][]string) WAFIdentification {
	result := WAFIdentification{}

	normalizedHeaders := make(map[string][]string)
	for k, v := range headers {
		normalizedHeaders[strings.ToLower(k)] = v
	}

	for provider, signatures := range wafHeaderSignatures {
		for _, sig := range signatures {
			values, exists := normalizedHeaders[sig.header]
			if !exists {
				continue
			}
			if sig.value == "" {
				result.Detected = true
				result.Provider = provider
				result.Details = append(result.Details, sig.header+": "+strings.Join(values, ", "))
				return result
			}
			for _, v := range values {
				if strings.Contains(strings.ToLower(v), sig.value) {
					result.Detected = true
					result.Provider = provider
					result.Details = append(result.Details, sig.header+": "+v)
					return result
				}
			}
		}
	}

	// Check Server header patterns
	if serverValues, exists := normalizedHeaders["server"]; exists {
		serverStr := strings.ToLower(strings.Join(serverValues, " "))
		for provider, patterns := range wafServerPatterns {
			for _, pattern := range patterns {
				if strings.Contains(serverStr, pattern) {
					result.Detected = true
					result.Provider = provider
					result.Details = append(result.Details, "server: "+serverStr)
					return result
				}
			}
		}
	}

	// Check Via header for CloudFront
	if viaValues, exists := normalizedHeaders["via"]; exists {
		viaStr := strings.ToLower(strings.Join(viaValues, " "))
		if strings.Contains(viaStr, "cloudfront") {
			result.Detected = true
			result.Provider = WAFCloudFront
			result.Details = append(result.Details, "via: "+strings.Join(viaValues, ", "))
			return result
		}
	}

	return result
}

// IdentifyWAFFromHistory detects WAF from a stored history item.
func IdentifyWAFFromHistory(history *db.History) WAFIdentification {
	headers, err := history.GetResponseHeadersAsMap()
	if err != nil {
		return WAFIdentification{}
	}
	return IdentifyWAF(headers)
}

