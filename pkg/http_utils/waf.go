// Generic block patterns that don't identify a specific WAF
var genericBlockPatterns = []string{
	"Request blocked",
	"Access Denied",
	"Forbidden",
	"Web Application Firewall",
	"Your request has been blocked",
	"This request has been blocked by the security rules",
	"security violation",
}

// IsWAFBlock checks if a response is a WAF block page.
// It returns the identification with IsBlocked and BlockType set.
func IsWAFBlock(history *db.History) WAFIdentification {
	waf := IdentifyWAFFromHistory(history)

	body, _ := history.ResponseBody()
	bodyStr := string(body)
	bodyLower := strings.ToLower(bodyStr)

	// If we know the WAF, check its specific block signatures
	if waf.Detected {
		if sig, exists := wafBlockSignatures[waf.Provider]; exists {
			statusMatch := false
			for _, code := range sig.statusCodes {
				if history.StatusCode == code {
					statusMatch = true
					break
				}
			}

			for _, pattern := range sig.bodyPatterns {
				if strings.Contains(bodyLower, strings.ToLower(pattern)) {
					waf.IsBlocked = true
					if history.StatusCode == 429 {
						waf.BlockType = "rate_limit"
					} else if strings.Contains(bodyLower, "challenge") || strings.Contains(bodyLower, "captcha") || strings.Contains(bodyLower, "turnstile") || strings.Contains(bodyLower, "just a moment") {
						waf.BlockType = "challenge"
					} else {
						waf.BlockType = "block"
					}
					waf.Details = append(waf.Details, "Block pattern: "+pattern)
					return waf
				}
			}

			// Status code match without body pattern — might still be a block
			// but lower confidence, don't mark as blocked without body evidence
			_ = statusMatch
		}
	}

	// If no WAF identified yet, check all block signatures
	if !waf.IsBlocked {
		for provider, sig := range wafBlockSignatures {
			for _, pattern := range sig.bodyPatterns {
				if strings.Contains(bodyLower, strings.ToLower(pattern)) {
					statusMatch := false
					for _, code := range sig.statusCodes {
						if history.StatusCode == code {
							statusMatch = true
							break
						}
					}
					if statusMatch {
						waf.Detected = true
						waf.Provider = provider
						waf.IsBlocked = true
						waf.BlockType = "block"
						waf.Details = append(waf.Details, "Block pattern: "+pattern)
						return waf
					}
				}
			}
		}
	}

	// Check generic block patterns on 403/429/503 HTML responses only
	// JSON/API responses with "Forbidden" are application-level errors, not WAF blocks
	isHTML := strings.Contains(strings.ToLower(history.ResponseContentType), "text/html")
	if !waf.IsBlocked && isHTML && (history.StatusCode == 403 || history.StatusCode == 429 || history.StatusCode == 503) {
		for _, pattern := range genericBlockPatterns {
			if strings.Contains(bodyLower, strings.ToLower(pattern)) {
				waf.IsBlocked = true
				waf.BlockType = "block"
				if !waf.Detected {
					waf.Provider = WAFUnknown
				}
				waf.Details = append(waf.Details, "Generic block pattern: "+pattern)
				return waf
			}
		}
	}

	return waf
}
