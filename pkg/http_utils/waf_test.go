func TestIsWAFBlock_NormalResponse(t *testing.T) {
	body := `<!DOCTYPE html><html><head><title>Welcome</title></head>
	<body><h1>Hello World</h1></body></html>`

	history := &db.History{
		StatusCode:          200,
		RawResponse:         []byte("HTTP/1.1 200 OK\r\nServer: nginx\r\n\r\n" + body),
		ResponseContentType: "text/html",
		ResponseBodySize:    len(body),
	}

	result := IsWAFBlock(history)
	assert.False(t, result.IsBlocked)
}

func TestIsWAFBlock_CloudflareNonBlock403(t *testing.T) {
	// A 403 from Cloudflare that is NOT a block (app-level 403)
	body := `{"error": "Forbidden", "message": "You do not have access to this resource"}`

	history := &db.History{
		StatusCode:          403,
		RawResponse:         []byte("HTTP/1.1 403 Forbidden\r\nServer: cloudflare\r\nCf-Ray: abc123\r\nContent-Type: application/json\r\n\r\n" + body),
		ResponseContentType: "application/json",
		ResponseBodySize:    len(body),
	}

	result := IsWAFBlock(history)
	// WAF is detected (Cloudflare) but the response is NOT a WAF block
	assert.True(t, result.Detected)
	assert.Equal(t, WAFCloudflare, result.Provider)
	assert.False(t, result.IsBlocked)
}

